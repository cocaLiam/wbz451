LedPort Color_Port_link (ColorList color)
{
  switch (color)
  {
  case RED:
    return RED_PORT;
  case GREEN:
    return GREEN_PORT;
  case BLUE:
    return BLUE_PORT;
  default:
    return -1;
  }
}

void LED_INIT( void )
{

  for (int i = 0; i < TOTAL; i++)
  {
    led_device[i].color = (ColorList)i;
    led_device[i].led_state = CURRENT_LED_OFF;
    led_device[i].port = Color_Port_link(led_device[i].color);

    PinMode(led_device[i].port , OUTPUT);
    printf("LED_INIT : %d, %d, %d\r\n",led_device[i].color,led_device[i].led_state,led_device[i].port);
  }


}



void LED_ON(ColorList color)
{
  led_device[color].led_state = CURRENT_LED_ON;
  PinSet(led_device[color].port , ON);
}


void LED_OFF(ColorList color)
{
  led_device[color].led_state = CURRENT_LED_OFF;
  PinSet(led_device[color].port , OFF);
}

void LED_TOGGLE(ColorList color)
{
  led_device[color].led_state = !(led_device[color].led_state);
  PinToggle(led_device[color].port);
}

